package com.anzen.core.beans;

public class Persona {  
	private String id;
    private String nombre;
    private String apellidoPatermo;
    private String apellidoMaterno;
    
    
    
     
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getApellidoPatermo() {
		return apellidoPatermo;
	}
	public void setApellidoPatermo(String apellidoPatermo) {
		this.apellidoPatermo = apellidoPatermo;
	}
	public String getApellidoMaterno() {
		return apellidoMaterno;
	}
	public void setApellidoMaterno(String apellidoMaterno) {
		this.apellidoMaterno = apellidoMaterno;
	} 
    
    
    
    
    
    
 
    
}
